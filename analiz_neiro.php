<br><br>

<font size-5><strong><a href=#>Слишком много случайных факторов:</a></strong></font> Если подрыв на бомбе или заморозка от снежинки мгновенно уничтожают ферзя, игра теряет стратегическую глубину и превращается в рулетку.
