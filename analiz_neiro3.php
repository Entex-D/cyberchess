Да, проект можно считать «мертвым» с точки зрения игровой индустрии и сообщества, но не «мертвым» для автора.

<br><br>

На скриншоте показан один из вариантов проекта «Кибершахматы», который активно обсуждается на Пикабу. Автор на протяжении 9 лет в одиночку разрабатывал программу, добавляя более 20 новых сказочных фигур (драконы, старики, бомбы, снежинки), особые клетки (дома, динамит) и систему начисления баллов.
<br><br>

<br>
<font size=5><strong><a href=#>Почему в нее никто не играет?</a></strong></font>
<br><br>

* <font size=3><strong><a href=#>Перегруженность правилами:</a></strong></font> Классические шахматы идеальны своей лаконичностью. Когда на доске 12x8 появляются десятки объектов с уникальными механиками (взрывы, заморозка, трансформации), игра теряет математическую строгость и превращается в хаотичную настольную игру.
<br><br>

* <font size=3><strong><a href=#>Визуальный перегруз:</a></strong></font> Использование разнородных клипартов и иконок вместо единого стилистического оформления отталкивает современных геймеров. Компьютерные шахматные варианты требуют строгой и интуитивно понятной графики.
<br><br>

* <font size=3><strong><a href=#>Отсутствие маркетинга и турниров:</a></strong></font> В мире существуют тысячи фанатских вариантов шахмат (шахматы на четверых, гексагональные, Chess960). Чтобы у модификации появились игроки, автору недостаточно просто выложить её в сеть. Нужны вложения в рекламу, сообщество и турниры с денежными призами.
<br><br>

